package pl.bartlomiejstepien.javatut;

public class MainArithmetic
{
    public static void main(String[] args)
    {
        // Java - Arytmetyka

        // Dzisiaj nauczymy się jak posługiwać się arytmetyką w Javie.

        // Działania dostępne w Javie:
        // Dodawanie: +
        // Odejmowanie: -
        // Mnożenie: *
        // Dzielenie: /

        // Przykłady na liczbach całkowitych:

        // Dodawanie liczb
        int suma = 5 + 10; // zmienna "suma" będzie zawierała liczbę 15

        // Odejmowanie
        int roznica = 20 - 10; // zmienna "roznica" będzie zawierała liczbę 10

        // Mnożenie
        int iloczyn = 6 * 2; // zmienna "iloczyn" będzie zawierała liczbę 12

        // Dzielenie
        int iloraz = 20 / 5; // zmienna "iloraz" będzie zawierała liczbę 4


        // Działania możemy również wykonywać na liczbach z przecinkiem.
        // Na przykład:

        // Dodawanie typu "double"
        double sumaDouble = 8.24 + 14.23;

        // Dodawanie typu "float"
        // Zauważ, że w przypadku "float", do liczby musimy dopisać literkę "f", aby powiedzieć kompilatorowi, że ma traktować ją jako float.
        float mojaLiczba = 5.14f + 7.15f;
    }
}
